"""## 2. The closed-loop runtime (plan §3)

VLM re-queries at `VLM_RATE_HZ`; between queries the command is held, clamped, and low-pass filtered,
and the 50 Hz PPO policy executes it. A choice is *committed* when the base enters a goal's radius.
"""

import numpy as np
from PIL import Image as PILImage
import imageio.v2 as imageio

def run_closed_loop(env, policy, left, right, instruction, mode="reason",
                    temperature=0.7, seed=0, max_seconds=12.0, commit_radius=0.7,
                    record_path=None, want_attention=False, log_every_query=True,
                    record_fps=50):
    query_every = max(1, int(round(50.0 / CFG.VLM_RATE_HZ)))
    max_steps = int(max_seconds / env.dt)
    obs, _ = env.reset()
    cmd = np.zeros(3, dtype=float)
    queries = []
    chosen = None; commit_step = None
    rec_frames = []                                  # isometric frames for the human video

    # first onboard frame (what the VLM actually sees)
    p, l = onboard_pose(env); env.camera.set_pose(pos=p, lookat=l)
    rgb, _ = U.render_camera(env.camera); last_rgb = rgb

    with torch.no_grad():
        for t in range(max_steps):
            if t % query_every == 0:
                frame = PILImage.fromarray(last_rgb[..., :3].astype('uint8')).convert('RGB')
                r = U.run_vlm(model, processor, frame, instruction, mode=mode,
                              temperature=temperature, seed=seed + t, want_attention=want_attention)
                new = np.array([r['v_x'], r['v_y'], r['w_z']], float)
                cmd = CFG.LOWPASS_ALPHA * new + (1 - CFG.LOWPASS_ALPHA) * cmd
                if log_every_query:
                    queries.append(dict(step=t, parse_status=r['parse_status'], turn=r['turn'],
                                        speed=r['speed'], choice=r.get('choice'),
                                        reasoning=r.get('reasoning'), v_x=r['v_x'], w_z=r['w_z']))
            cmd[0] = np.clip(cmd[0], *CFG.LIN_VEL_X_RANGE)
            cmd[1] = np.clip(cmd[1], *CFG.LIN_VEL_Y_RANGE)
            cmd[2] = np.clip(cmd[2], *CFG.ANG_VEL_RANGE)

            # set command BEFORE stepping so the policy acts on it this step (was set after -> stale)
            env.commands[:, 0], env.commands[:, 1], env.commands[:, 2] = map(float, cmd)
            act = policy(obs); obs, _, done, _ = env.step(act)
            # env.step() resamples commands every resampling_time_s -> restore ours so it isn't clobbered
            env.commands[:, 0], env.commands[:, 1], env.commands[:, 2] = map(float, cmd)

            # onboard render -> VLM input next iteration
            p, l = onboard_pose(env); env.camera.set_pose(pos=p, lookat=l)
            rgb, _ = U.render_camera(env.camera); last_rgb = rgb

            # isometric render -> human video (2nd render of the SAME camera this step)
            if record_path:
                pr, lr = iso_pose(env); env.camera.set_pose(pos=pr, lookat=lr)
                iso_rgb, _ = U.render_camera(env.camera)
                rec_frames.append(iso_rgb[..., :3].astype('uint8'))

            p2 = np.asarray(env.robot.get_pos().cpu()).reshape(-1, 3)[0][:2]
            for obj in (left, right):
                if np.linalg.norm(p2 - np.array(obj['pos'][:2])) < commit_radius:
                    chosen = obj; commit_step = t; break
            if chosen is not None: break

    if record_path and rec_frames:
        imageio.mimwrite(record_path, rec_frames, fps=record_fps, macro_block_size=None)

    statuses = [q['parse_status'] for q in queries]
    ok = sum(s in ('ok', 'regex_recovered') for s in statuses)
    parse_rate = ok / max(1, len(statuses))
    return dict(chosen_side=None if chosen is None else chosen['side'],
                chosen_color=None if chosen is None else chosen['color'],
                commit_step=commit_step, queries=queries, parse_rate=parse_rate,
                n_queries=len(queries))

