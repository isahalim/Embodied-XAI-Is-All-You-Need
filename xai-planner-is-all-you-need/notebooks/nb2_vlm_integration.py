# --- Camera helpers: VLM gets a first-person onboard view; humans get an isometric view ---
import numpy as np

def _robot_yaw(env):
    q = np.asarray(env.robot.get_quat().cpu()).reshape(-1)[:4]   # Genesis quat = (w, x, y, z)
    w, x, y, z = q
    return float(np.arctan2(2*(w*z + x*y), 1 - 2*(y*y + z*z)))    # yaw only -> no gait bobbing

def _yaw_R(yaw):
    c, s = np.cos(yaw), np.sin(yaw)
    return np.array([[c, -s, 0.0], [s, c, 0.0], [0.0, 0.0, 1.0]])

def onboard_pose(env):
    """First-person: camera just ahead of the body, looking forward along the heading."""
    base = np.asarray(env.robot.get_pos().cpu()).reshape(-1, 3)[0]
    R = _yaw_R(_robot_yaw(env))
    pos  = base + R @ np.array([0.30, 0.0, 0.15])   # ahead & slightly up (robot stays out of frame)
    look = base + R @ np.array([3.0,  0.0, 0.0])    # straight ahead, level
    return tuple(pos.tolist()), tuple(look.tolist())

def iso_pose(env):
    """Elevated 3/4 'isometric' view that follows the robot at a fixed world angle."""
    base = np.asarray(env.robot.get_pos().cpu()).reshape(-1, 3)[0]
    pos  = base + np.array([2.8, 2.8, 2.2])
    look = base + np.array([0.0, 0.0, 0.25])
    return tuple(pos.tolist()), tuple(look.tolist())

